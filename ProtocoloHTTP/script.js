
let Foto1 = "Icono1";

function CambioImagen1(){

	let Img1 = document.getElementById("F1");

	if (Foto1 == "Icono1"){
		Img1.src = "Imagenes/Icono2.png";
		Foto1 = "Icono2";
	}
	else {
		Img1.src = "Imagenes/Icono1.png";
		Foto1 = "Icono1";
	} 

}

